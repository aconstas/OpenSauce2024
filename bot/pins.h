#ifndef PINS_H
#define PINS_H

// Pin definitions
#define ESC_LEFT_PIN (9)        // Pin for the ESC left motor
#define ESC_LEFT_REV_PIN (6)    // Pin for the ESC left motor reverse
#define ESC_RIGHT_PIN (10)      // Pin for the ESC right motor
#define ESC_RIGHT_REV_PIN (11)  // Pin for the ESC right motor reverse


#define PHOTO_PIN_1 (15)  // A0 - First photo sensor
#define PHOTO_PIN_2 (16)  // A2 - Second photo sensor
#define PHOTO_PIN_3 (14)  // A1 - Third photo sensor

#define LED_R_PIN (3)
#define LED_G_PIN (5)
#define LED_B_PIN (8)

#endif  // PINS_H
